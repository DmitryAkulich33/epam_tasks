package by.epam.bakery.controller;

import by.epam.bakery.dao.ClientDAOImpl;
import by.epam.bakery.dao.PieDAOImpl;
import by.epam.bakery.domain.Client;
import by.epam.bakery.domain.Pie;

import java.util.List;

public class Runner {
    public static void main(String[] args) {
        ClientDAOImpl clientDAOImpl = new ClientDAOImpl();
        PieDAOImpl pieDAOImpl = new PieDAOImpl();
        List<Client> clients;
        List<Pie> pies;

        clientDAOImpl.deleteClient(2);
        clientDAOImpl.changeNote(3, "Yo");
        pieDAOImpl.deletePie(2);
        System.out.println();
        clients = clientDAOImpl.findAll();
        for(Client client : clients) {
            System.out.println(client);
        }
        pies = pieDAOImpl.findAll();
        for(Pie pie : pies) {
            System.out.println(pie);
        }
        pies = pieDAOImpl.findPieByWeight(1000);
        for(Pie pie : pies) {
            System.out.println(pie);
        }
    }
}
